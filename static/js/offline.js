const originalSearchRides = window.searchRides;
window.searchRides = async function() {
    if (!offlineManager.isOnline) {
        const from = document.getElementById('fromSearch').value;
        const to = document.getElementById('toSearch').value;
        const results = offlineManager.searchRidesOffline(from, to);
        
        const resultsContainer = document.getElementById('results');
        if (results.length === 0) {
            resultsContainer.innerHTML = '<p>üì± Offline re≈æim - ≈æ√°dn√© ulo≈æen√© j√≠zdy nenalezeny.</p>';
        } else {
            let html = '<h3>üì± Offline v√Ωsledky:</h3>';
            results.forEach(ride => {
                html += `
                    <div class="ride-item" style="border-left-color: #ffc107;">
                        <h4>üöó ${ride.driver_name} ${ride.offline ? '(offline)' : ''}</h4>
                        <p><strong>Trasa:</strong> ${ride.from_location} ‚Üí ${ride.to_location}</p>
                        <p><strong>Cena:</strong> ${ride.price_per_person} Kƒç</p>
                        <p style="color: #ffc107;"><small>‚ö†Ô∏è Offline data - m≈Ø≈æe b√Ωt zastaral√©</small></p>
                    </div>
                `;
            });
            resultsContainer.innerHTML = html;
        }
        return;
    }
    
    // Online re≈æim
    return originalSearchRides.apply(this, arguments);
};

