// Offline re≈æim pro z√°kladn√≠ funkce
class OfflineManager {
    constructor() {
        this.isOnline = navigator.onLine;
        this.offlineData = {
            rides: [],
            messages: [],
            locations: {}
        };
        this.init();
    }

    init() {
        // Poslouch√° zmƒõny p≈ôipojen√≠
        window.addEventListener('online', () => this.handleOnline());
        window.addEventListener('offline', () => this.handleOffline());
        
        // Naƒçte offline data z localStorage
        this.loadOfflineData();
        
        // Zobraz√≠ status p≈ôipojen√≠
        this.updateConnectionStatus();
    }

    handleOnline() {
        this.isOnline = true;
        this.updateConnectionStatus();
        this.syncOfflineData();
        this.showNotification('‚úÖ P≈ôipojen√≠ obnoveno', 'Synchronizuji offline data...');
    }

    handleOffline() {
        this.isOnline = false;
        this.updateConnectionStatus();
        this.showNotification('‚ö†Ô∏è Offline re≈æim', 'Nƒõkter√© funkce jsou omezen√©');
    }

    updateConnectionStatus() {
        const statusElement = document.getElementById('connectionStatus');
        if (statusElement) {
            statusElement.textContent = this.isOnline ? 'P≈ôipojeno' : 'Offline re≈æim';
            statusElement.className = this.isOnline ? 'connected' : 'disconnected';
        }
    }

    // Ulo≈æ√≠ data pro offline pou≈æit√≠
    saveOfflineData(key, data) {
        this.offlineData[key] = data;
        localStorage.setItem('spolujizda_offline', JSON.stringify(this.offlineData));
    }

    // Naƒçte offline data
    loadOfflineData() {
        const saved = localStorage.getItem('spolujizda_offline');
        if (saved) {
            try {
                this.offlineData = JSON.parse(saved);
            } catch (e) {
                console.error('Chyba p≈ôi naƒç√≠t√°n√≠ offline dat:', e);
            }
        }
    }

    // Synchronizuje offline data p≈ôi obnoven√≠ p≈ôipojen√≠
    async syncOfflineData() {
        const pendingActions = this.getPendingActions();
        
        for (const action of pendingActions) {
            try {
                await fetch(action.url, action.options);
                console.log('Offline akce synchronizov√°na:', action);
            } catch (error) {
                console.error('Chyba p≈ôi synchronizaci:', error);
            }
        }
        
        // Vyma≈æe synchronizovan√© akce
        this.clearPendingActions();
    }

    // P≈ôid√° akci do fronty pro offline synchronizaci
    addPendingAction(url, options) {
        const actions = this.getPendingActions();
        actions.push({
            url,
            options,
            timestamp: Date.now()
        });
        localStorage.setItem('pending_actions', JSON.stringify(actions));
    }

    getPendingActions() {
        const saved = localStorage.getItem('pending_actions');
        return saved ? JSON.parse(saved) : [];
    }

    clearPendingActions() {
        localStorage.removeItem('pending_actions');
    }

    // Offline vyhled√°v√°n√≠ j√≠zd
    searchRidesOffline(from, to) {
        const cachedRides = this.offlineData.rides || [];
        
        // Jednoduch√© filtrov√°n√≠ podle textu
        const results = cachedRides.filter(ride => 
            ride.from_location.toLowerCase().includes(from.toLowerCase()) ||
            ride.to_location.toLowerCase().includes(to.toLowerCase())
        );

        return results;
    }

    // Offline nab√≠dka j√≠zdy
    offerRideOffline(rideData) {
        // Ulo≈æ√≠ do offline fronty
        this.addPendingAction('/api/rides/offer', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(rideData)
        });

        // P≈ôid√° do lok√°ln√≠ch dat pro okam≈æit√© zobrazen√≠
        const localRide = {
            ...rideData,
            id: Date.now(),
            driver_name: 'Vy (ƒçek√° na synchronizaci)',
            offline: true
        };
        
        this.offlineData.rides.push(localRide);
        this.saveOfflineData('rides', this.offlineData.rides);

        return localRide;
    }

    // Offline zpr√°vy
    sendMessageOffline(messageData) {
        this.addPendingAction('/api/messages/send', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(messageData)
        });

        // Ulo≈æ√≠ lok√°lnƒõ
        const localMessage = {
            ...messageData,
            id: Date.now(),
            timestamp: new Date().toISOString(),
            offline: true
        };

        if (!this.offlineData.messages) {
            this.offlineData.messages = [];
        }
        this.offlineData.messages.push(localMessage);
        this.saveOfflineData('messages', this.offlineData.messages);

        return localMessage;
    }

    // Offline mapa - z√°kladn√≠ funkcionalita
    initOfflineMap() {
        if (!this.isOnline && typeof L !== 'undefined') {
            // P≈ôepne na offline tile layer (pokud je dostupn√Ω)
            const offlineLayer = L.tileLayer('', {
                attribution: 'Offline re≈æim - omezen√° funkcionalita'
            });
            
            if (map && map.hasLayer) {
                // Odebere online vrstvy
                map.eachLayer(layer => {
                    if (layer instanceof L.TileLayer) {
                        map.removeLayer(layer);
                    }
                });
                
                // P≈ôid√° offline vrstvu
                offlineLayer.addTo(map);
            }
        }
    }

    // Zobraz√≠ notifikaci
    showNotification(title, body) {
        if ('Notification' in window && Notification.permission === 'granted') {
            new Notification(title, { body });
        } else {
            // Fallback - zobraz√≠ alert
            console.log(`${title}: ${body}`);
        }
    }

    // Exportuje data pro backup
    exportData() {
        const exportData = {
            rides: this.offlineData.rides,
            messages: this.offlineData.messages,
            userStats: localStorage.getItem('user_stats'),
            timestamp: new Date().toISOString()
        };

        const blob = new Blob([JSON.stringify(exportData, null, 2)], {
            type: 'application/json'
        });
        
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = `spolujizda_backup_${new Date().toISOString().split('T')[0]}.json`;
        a.click();
        URL.revokeObjectURL(url);
    }

    // Importuje data ze z√°lohy
    importData(file) {
        const reader = new FileReader();
        reader.onload = (e) => {
            try {
                const importedData = JSON.parse(e.target.result);
                
                // Slouƒç√≠ s existuj√≠c√≠mi daty
                this.offlineData.rides = [...(this.offlineData.rides || []), ...(importedData.rides || [])];
                this.offlineData.messages = [...(this.offlineData.messages || []), ...(importedData.messages || [])];
                
                this.saveOfflineData('rides', this.offlineData.rides);
                this.saveOfflineData('messages', this.offlineData.messages);
                
                if (importedData.userStats) {
                    localStorage.setItem('user_stats', importedData.userStats);
                }
                
                alert('‚úÖ Data √∫spƒõ≈°nƒõ importov√°na!');
            } catch (error) {
                alert('‚ùå Chyba p≈ôi importu dat: ' + error.message);
            }
        };
        reader.readAsText(file);
    }

    // Vyma≈æe offline data
    clearOfflineData() {
        if (confirm('Opravdu chcete vymazat v≈°echna offline data?')) {
            this.offlineData = { rides: [], messages: [], locations: {} };
            localStorage.removeItem('spolujizda_offline');
            localStorage.removeItem('pending_actions');
            alert('‚úÖ Offline data vymaz√°na');
        }
    }
}

